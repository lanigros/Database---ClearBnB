package datatransforobject;

import model.User;

public class UserCoreDTO {

  private int id;
  private String firstName;
  private String lastName;
  private String email;
  private String password;

  public UserCoreDTO() {

  }

  public UserCoreDTO(int id, String firstName, String lastName, String email, String password) {
    this.id = id;
    this.firstName = firstName;
    this.lastName = lastName;
    this.email = email;
    this.password = password;
  }

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getFirstName() {
    return firstName;
  }

  public void setFirstName(String firstName) {
    this.firstName = firstName;
  }

  public String getLastName() {
    return lastName;
  }

  public void setLastName(String lastName) {
    this.lastName = lastName;
  }

  public String getEmail() {
    return email;
  }

  public void setEmail(String email) {
    this.email = email;
  }

  public String getPassword() {
    return password;
  }

  public void setPassword(String password) {
    this.password = password;
  }

  public User convertToUser() {
    return new User(this.firstName, this.lastName, this.email, this.password);
  }


}
